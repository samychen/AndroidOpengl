    private static final int INDEX_ORIENTATION = 0;
    private static final String[] IMAGE_PROJECTION = new String[] {
        ImageColumns.ORIENTATION
    };
    
    public static int getOrientation(ExifInterface exif) {
        int degree = 0;

        if (exif != null) {
            int orientation = exif.getAttributeInt(
                ExifInterface.TAG_ORIENTATION, -1);
            if (orientation != -1) {
                // We only recognize a subset of orientation tag values.
                switch(orientation) {
                    case ExifInterface.ORIENTATION_ROTATE_90:
                        degree = 90;
                        break;
                    case ExifInterface.ORIENTATION_ROTATE_180:
                        degree = 180;
                        break;
                    case ExifInterface.ORIENTATION_ROTATE_270:
                        degree = 270;
                        break;
                }

            }
        }
        return degree;
    }
    
    public static int getOrientation(String path) {
    	int res = 0;
    	try {
			ExifInterface exif = new ExifInterface(path);
			res = getOrientation(exif);
		} catch (IOException e) {
			e.printStackTrace();
		}

    	return res;
    }
    


    public static int getOrientation(Uri uri, Context context) {
        int orientation = 0;
        Cursor cursor = null;
        try {
            cursor = context.getContentResolver().query(uri, IMAGE_PROJECTION, null, null, null);
            if ((cursor != null) && cursor.moveToNext()) {
                orientation = cursor.getInt(INDEX_ORIENTATION);
            }
        } catch (Exception e) {
            // Ignore error for no orientation column; just use the default orientation value 0.
        } finally {
            if (cursor != null) {
                cursor.close();
            }
        }
        return orientation;
    }
    
    public static int getOrientation(byte[] jpeg) {
        if (jpeg == null) {
            return 0;
        }

        int offset = 0;
        int length = 0;

        // ISO/IEC 10918-1:1993(E)
        while (offset + 3 < jpeg.length && (jpeg[offset++] & 0xFF) == 0xFF) {
            int marker = jpeg[offset] & 0xFF;

            // Check if the marker is a padding.
            if (marker == 0xFF) {
                continue;
            }
            offset++;

            // Check if the marker is SOI or TEM.
            if (marker == 0xD8 || marker == 0x01) {
                continue;
            }
            // Check if the marker is EOI or SOS.
            if (marker == 0xD9 || marker == 0xDA) {
                break;
            }

            // Get the length and check if it is reasonable.
            length = pack(jpeg, offset, 2, false);
            if (length < 2 || offset + length > jpeg.length) {
            	LogUtil.logE(TAG, "Invalid length");
                return 0;
            }

            // Break if the marker is EXIF in APP1.
            if (marker == 0xE1 && length >= 8 &&
                    pack(jpeg, offset + 2, 4, false) == 0x45786966 &&
                    pack(jpeg, offset + 6, 2, false) == 0) {
                offset += 8;
                length -= 8;
                break;
            }

            // Skip other markers.
            offset += length;
            length = 0;
        }

        // JEITA CP-3451 Exif Version 2.2
        if (length > 8) {
            // Identify the byte order.
            int tag = pack(jpeg, offset, 4, false);
            if (tag != 0x49492A00 && tag != 0x4D4D002A) {
            	LogUtil.logE(TAG, "Invalid byte order");
                return 0;
            }
            boolean littleEndian = (tag == 0x49492A00);

            // Get the offset and check if it is reasonable.
            int count = pack(jpeg, offset + 4, 4, littleEndian) + 2;
            if (count < 10 || count > length) {
            	LogUtil.logE(TAG, "Invalid offset");
                return 0;
            }
            offset += count;
            length -= count;

            // Get the count and go through all the elements.
            count = pack(jpeg, offset - 2, 2, littleEndian);
            while (count-- > 0 && length >= 12) {
                // Get the tag and check if it is orientation.
                tag = pack(jpeg, offset, 2, littleEndian);
                if (tag == 0x0112) {
                    // We do not really care about type and count, do we?
                    int orientation = pack(jpeg, offset + 8, 2, littleEndian);
                    switch (orientation) {
                        case 1:
                            return 0;
                        case 3:
                            return 180;
                        case 6:
                            return 90;
                        case 8:
                            return 270;
                    }
                    LogUtil.logV(TAG, "Unsupported orientation");
                    return 0;
                }
                offset += 12;
                length -= 12;
            }
        }

        LogUtil.logV(TAG, "Orientation not found");
        return 0;
    }

    private static int pack(byte[] bytes, int offset, int length,
            boolean littleEndian) {
        int step = 1;
        if (littleEndian) {
            offset += length - 1;
            step = -1;
        }

        int value = 0;
        while (length-- > 0) {
            value = (value << 8) | (bytes[offset] & 0xFF);
            offset += step;
        }
        return value;
    }

    public static PhotoImage  getPhotExif(Context context,String path){
        PhotoImage mPhotoImage=new PhotoImage();
        ExifInterface exifInterface = null;
        try {
            exifInterface = new ExifInterface(path);
            String FFNumber = exifInterface.getAttribute(ExifInterface.TAG_APERTURE);
            String FDateTime = exifInterface.getAttribute(ExifInterface.TAG_DATETIME);
            String FExposureTime = exifInterface.getAttribute(ExifInterface.TAG_EXPOSURE_TIME);
            String FFlash = exifInterface.getAttribute(ExifInterface.TAG_FLASH);
            Double FFocalLength = exifInterface.getAttributeDouble(ExifInterface.TAG_FOCAL_LENGTH, -1);
            String FImageWidth = exifInterface.getAttribute(ExifInterface.TAG_IMAGE_WIDTH);
            String FImageLength = exifInterface.getAttribute(ExifInterface.TAG_IMAGE_LENGTH);
            String FISOSpeedRatings = exifInterface.getAttribute(ExifInterface.TAG_ISO);
            String FMake = exifInterface.getAttribute(ExifInterface.TAG_MAKE);
            String FModel = exifInterface.getAttribute(ExifInterface.TAG_MODEL);
            int FOrientation = exifInterface.getAttributeInt(ExifInterface.TAG_ORIENTATION, -1);
            int FWhiteBalance = exifInterface.getAttributeInt(ExifInterface.TAG_WHITE_BALANCE, -1);

            long length = (new File(path)).length();
            String lengthString = length < 0 ? null : Formatter.formatFileSize(context, length);

            String title = path.substring((path.lastIndexOf("/") + 1), path.length() - 4);

            mPhotoImage.setName(title);
            mPhotoImage.setPhotoMarker(FMake);
            mPhotoImage.setPhotoModel(FModel);
            mPhotoImage.setPhotoDatetime(FDateTime);
            mPhotoImage.setSize(lengthString);
            mPhotoImage.setPhotoImageWidth(FImageWidth);
            mPhotoImage.setPhotoImageLength(FImageLength);
            mPhotoImage.setPhotoOrientation(FOrientation);
            mPhotoImage.setPhotoExposureTime(FExposureTime);
            mPhotoImage.setPhotoIso(FISOSpeedRatings);
            mPhotoImage.setPhotoAperture(FFNumber);
            mPhotoImage.setPhotoFocalLength(FFocalLength);
            mPhotoImage.setPhotoWhiteBalance(FWhiteBalance);
            mPhotoImage.setPhotoFlash(FFlash);
            mPhotoImage.setPath(path);

        } catch (IOException e) {
            e.printStackTrace();
        }

        return  mPhotoImage;
    }

